# Basic Data Types & Variables

# Integer & Float
x = 10
y = 20.14

print(x * y)

# String
name = "Mohit"
print("Heelo, " + name)


# List
#          0           1         2
fruits = ["apple", "banana", "cherry"]
#            -3         -2         -1
print(fruits[1])
print(fruits[-1])

# Dict (HashMap<String, String>)
person = {
    "name": "Mohit",
    "email": "test@gmail.com",
    "pincode": 134113,
    "rating": 9.99
}
print(person["email"])


